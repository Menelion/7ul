    <script>
        function copyClipBoard(element, title) {
            var copyText = $(element);
            copyText.select();
            document.execCommand("copy");
            iziToast.show({
                message: title + ' {{ __('copied successfully.') }}',
                position: 'bottomRight',
                color: 'blue'
            });
        }
    </script>
